TIC=function()
  --{
  e=e+3
  n=.5+(r^2)^.5
  u=u-r/24
  r=u/24+r
  --}
  for f=0,33120 do
    --{
    poke(f%48+16320,f%48*6*math.sin(f%48)^2)
    d=f//240
    k=f%240
    --}
    --{
    q=d/66-1
    j=k/66-u-1.8
    --}
    h=(q*q+j^2)^.5/n
    if h<1then
      i=100/h*(1-(1-h)^.5)/n
      pix(k,d,(1-h)*((m(i*q+1,i*j)-m(i*q,i*j))*i*q+i*j*(m(i*q,i*j+1)-m(i*q,i*j))+n*14)+n*(2*d-k%2)%4/4+1)
    else
      pix(k,d,7*m(k,d)+7*m(k*.5,d*.5))
    end
  end
end
e=0
